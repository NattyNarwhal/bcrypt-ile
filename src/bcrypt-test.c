static void
simple_translate (const char *src, char *dst, const char *table, size_t length)
{
	for (size_t i = 0; i < length; i++) {
#ifdef __OS400__
		dst[i] = table[(unsigned char)src[i]];
#else
		dst[i] = src[i];
#endif
	}
}
#include <string.h>
#include <errno.h>

// Do NOT build with LOCALEUTF, we do manual conversions so EBCDIC argv works

int main(int argc, char **argv)
{
	char hashed[256], hashed_conv[256], *pw, *hash, *opt;
	int ret = 0;
	// "password" in ASCII (frame of reference)
	const char password[] = { 0x70, 0x61, 0x73, 0x73, 0x77, 0x6f, 0x72, 0x64, 0x0 };
	if (argc <= 1) {
		fprintf(stderr, "need first parm to be check, new, or test\n");
		return 1;
	}
	if (strcasecmp(argv[1], "check") == 0) {
		pw = strdup(argv[2]);
		simple_translate((const char*)argv[2], pw, e2a_table, strlen(pw));
		hash = strdup(argv[3]);
		simple_translate((const char*)argv[3], hash, e2a_table, strlen(hash));
		ret = crypt_checkpass(pw, hash);
		printf("checkpass %d errno %d\n", ret, errno);
	} else if (strcasecmp(argv[1], "new") == 0) {
		pw = strdup(argv[2]);
		simple_translate((const char*)argv[2], pw, e2a_table, strlen(pw));
		if (argc >= 4) {
			opt = strdup(argv[3]);
			simple_translate((const char*)argv[3], opt, e2a_table, strlen(pw));
		} else
			opt = NULL;
		crypt_newhash(pw, opt, hashed, 256);
		simple_translate((const char*)hashed, hashed_conv, a2e_table, strlen(hashed));
		puts(hashed_conv);
	} else if (strcasecmp(argv[1], "test") == 0) {
		crypt_newhash(password, NULL, hashed, 256);
		simple_translate((const char*)hashed, hashed_conv, a2e_table, strlen(hashed));
		puts(hashed_conv);
		ret = crypt_checkpass(password, hashed);
		printf("checkpass %d errno %d\n", ret, errno);
	}
	return ret;
}
